package cn.softbank.purchase.domain;

public class BaseGoods {
	private String wid;
	private String wname;
	private String wsku;
	private String wimg;
	private String woriginalprice;
	private String wsellingprice;
	private String nub;
	
	public String getWid() {
		return wid;
	}

	public void setWid(String wid) {
		this.wid = wid;
	}

	public String getWname() {
		return wname;
	}

	public void setWname(String wname) {
		this.wname = wname;
	}

	public String getWsku() {
		return wsku;
	}

	public void setWsku(String wsku) {
		this.wsku = wsku;
	}

	public String getWimg() {
		return wimg;
	}

	public void setWimg(String wimg) {
		this.wimg = wimg;
	}

	public String getWoriginalprice() {
		return woriginalprice;
	}

	public void setWoriginalprice(String woriginalprice) {
		this.woriginalprice = woriginalprice;
	}

	public String getWsellingprice() {
		return wsellingprice;
	}

	public void setWsellingprice(String wsellingprice) {
		this.wsellingprice = wsellingprice;
	}

	public String getNub() {
		return nub;
	}

	public void setNub(String nub) {
		this.nub = nub;
	}

}
